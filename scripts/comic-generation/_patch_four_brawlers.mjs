function applyPatches(id) {
  const file = path.join(__dirname, `build-${id}-script-v2.mjs`);
  let src = fs.readFileSync(file, "utf8");
  const map = patches[id] || {};
  for (const [oldText, newText] of Object.entries(map)) {
    src = src.split(oldText).join(newText);
  }
  fs.writeFileSync(file, src, "utf8");
  console.log("Patched", file);
}

for (const id of ["sora", "rin", "taro"]) applyPatches(id);
