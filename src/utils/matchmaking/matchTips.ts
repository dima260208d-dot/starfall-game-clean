export function pickRandomMatchTip(
  exclude?: string,
  locale: LocaleCode = getLocale(),
): string {
  const pool = locale === "en" ? MATCHMAKING_TIPS_EN : MATCHMAKING_TIPS_RU;
  if (pool.length === 0) return "";
  if (pool.length === 1) return pool[0]!;
  let tip = pool[Math.floor(Math.random() * pool.length)]!;
  let guard = 0;
  while (tip === exclude && guard++ < 12) {
    tip = pool[Math.floor(Math.random() * pool.length)]!;
  }
  return tip;
}

